#include <unistd.h>

void	ft_is_negative(int x)
{
	char	s;
	char	n;

	s = 'P';
	n = 'N';
	if (x >= 0)
	{
		write(1, &s, 1);
	}
	else
	{
		write(1, &n, 1);
	}
}
