#include <stdio.h>

int	ft_sqrt(int nb)
{
	long	numb;

	numb = 0;
	while ((numb * numb) <= nb)
	{
		if ((numb * numb) == nb)
			return (numb);
		numb++;
	}
	return (0);
}
